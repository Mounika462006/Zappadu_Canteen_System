export const getMenuByShop = (shopId: string) => menuItems.filter(item => item.shopId === shopId);

export const getCategoriesByShop = (shopId: string) => {
  const items = getMenuByShop(shopId);
  const cats = Array.from(new Set(items.map(i => i.category)));
  return ["All", ...cats];
};
